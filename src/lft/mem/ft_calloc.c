#include "libft.h"

void	*ft_calloc(const size_t n, const size_t sz) {
	size_t	asize;
	void	*out;

	if (!n | !sz)
		return NULL;
	asize = n * sz;
	if (asize / n != sz)
		return NULL;
	out = ft_malloc(asize);
	return (out) ? ft_memset(out, 0, asize) : NULL;
}
