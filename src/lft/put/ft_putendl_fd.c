#include "libft.h"

ssize_t	ft_putendl_fd(const char *s, const i32 fd) {
	ssize_t	rv;

	rv = ft_putstr_fd(s, fd);
	return (rv != -1 && ft_putchar_fd('\n', fd) != -1) ? rv + 1 : -1;
}
