#include "libft.h"

ssize_t	ft_putsnbr_fd(const i64 n, const i32 fd) {
	ssize_t	rv;

	if (n == INT64_MIN)
		return ft_write(fd, "-9223372036854775808", 20);
	if (n < 0) {
		if (ft_putchar_fd('-', fd) == -1)
			return -1;
		rv = ft_putsnbr_fd(-n, fd);
		return (rv != -1) ? rv + 1 : -1;
	}
	if (n > 9) {
		rv = ft_putsnbr_fd(n / 10, fd);
		return (rv != -1 && ft_putchar_fd(n % 10 + '0', fd) != -1) ? rv + 1 : -1;
	}
	return ft_putchar_fd(n + '0', fd);
}
