#include "libft.h"

ssize_t	ft_putunbr_fd(const u64 n, const i32 fd) {
	ssize_t	rv;

	if (n > 9) {
		rv = ft_putunbr_fd(n / 10, fd);
		return (rv != -1 && ft_putchar_fd(n % 10 + '0', fd) != -1) ? rv + 1 : -1;
	}
	return ft_putchar_fd(n + '0', fd);
}
