#include "libft.h"

char	*ft_stpcpy(char * restrict dst, const char * restrict src) {
	size_t	len;

	len = ft_strlen(src);
	ft_memcpy(dst, src, len);
	dst[len] = '\0';
	return &dst[len];
}
