#include "libft.h"

char	*ft_stpncpy(char * restrict dst, const char * restrict src, size_t n) {
	size_t	len;

	len = ft_strnlen(dst, n);
	return ft_memset((void *)((uintptr_t)ft_memcpy(dst, src, len) + len), 0, n - len);
}
