#include "libft.h"

char	*ft_strjoin(const char *s1, const char *s2) {
	size_t	len1;
	size_t	len2;
	char	*out;

	if (!s1 | !s2)
		return NULL;
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	out = ft_malloc((len1 + len2 + 1) * sizeof(*out));
	if (out)
		ft_strcat(ft_stpcpy(out, s1), s2);
	return out;
}
