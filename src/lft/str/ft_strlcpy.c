#include "libft.h"

size_t	ft_strlcpy(char * restrict dst, const char * restrict src, size_t n) {
	size_t	len;

	len = ft_strnlen(src, n - 1);
	ft_memcpy(dst, src, len);
	dst[len] = '\0';
	return ft_strlen(src);
}
