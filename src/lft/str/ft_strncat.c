#include "libft.h"

char	*ft_strncat(char * restrict dst, const char * restrict src, size_t n) {
	size_t	dlen;
	size_t	slen;

	dlen = ft_strlen(dst);
	slen = ft_strnlen(src, n);
	ft_memcpy(((void *)((uintptr_t)dst + dlen)), src, slen);
	dst[dlen + slen] = '\0';
	return dst;
}
