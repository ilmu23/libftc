#include "libft.h"

char	*ft_strndup(const char *s, size_t n) {
	size_t	asize;
	char	*out;

	if (!s)
		return NULL;
	asize = (ft_strnlen(s, n) + 1) * sizeof(*out);
	out = ft_malloc(asize);
	if (out)
		ft_strlcpy(out, s, asize);
	return out;
}
