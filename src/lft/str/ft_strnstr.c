#include "libft.h"

char	*ft_strnstr(const char *s1, const char *s2, size_t n) {
	size_t	l1;
	size_t	l2;
	size_t	i;
	size_t	j;

	if (!*s2)
		return (char *)s1;
	l1 = ft_strlen(s1);
	l2 = ft_strlen(s2);
	for (i = j = 0; (i < l1) & (j < l2) & (i + l2 - j <= n); i++) {
		if (s1[i] == s2[j])
			j++;
		else
			j = 0;
	}
	return (j == l2) ? (char *)&s1[i - l2] : NULL;
}
