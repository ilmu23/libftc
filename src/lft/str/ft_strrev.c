#include "libft.h"

void	ft_strrev(char *s) {
	size_t	i;
	size_t	j;

	j = ft_strlen(s);
	if (j == 0)
		return ;
	for (i = 0, j--; i < j; i++, j--)
		if (s[i] != s[j])
			swap(s[i], s[j]);
}
