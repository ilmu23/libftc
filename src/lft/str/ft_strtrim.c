#include "libft.h"

char	*ft_strtrim(const char *s, const char *set) {
	size_t	i;
	size_t	j;

	if (!s)
		return NULL;
	for (i = 0; s[i] && ft_strchr(set, s[i]); i++)
		;
	for (j = ft_strlen(s); j - 1 > i && s[j - 1] && ft_strchr(set, s[j - 1]); j--)
		;
	return ft_substr(s, i, j - i);
}
