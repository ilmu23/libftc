#include "libft.h"

char	*ft_substr(const char *s, u64 start, size_t len) {
	size_t	_len;
	char	*out;

	if (!s)
		return NULL;
	_len = ft_strlen(s);
	if (_len < start)
		return ft_strdup("");
	_len = ft_strnlen(&s[start], len) + 1;
	out = ft_malloc((_len) * sizeof(*out));
	if (out)
		ft_strlcpy(out, &s[start], _len);
	return out;
}
